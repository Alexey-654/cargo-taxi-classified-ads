<p>Услуга грузовое такси появилась вслед  за изменением динамики жизни и деловой активности городского населения. Основное ее отличие от других типов грузоперевозок:</p>
<ul class="myul">
  <li>быстрая подача автомобиля</li>
  <li>не требуется предварительного заказа и бронирования авто</li>
  <li>небольшие грузы по весу и объему для которых подойдет любой автомобиль типа Газель</li>
  <li>перевозка имеет разовый характер</li>
  <li>доставка грузов от двери к двери</li>
